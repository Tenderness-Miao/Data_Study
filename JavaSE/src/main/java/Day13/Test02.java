package Day13;

public class Test02 {
    public static void main(String[] args) {
        System.out.println(Math.PI);
        // 平方根
        System.out.println(Math.sqrt(4));
        // 大于等于0，小于1的double类型的随机数
        System.out.println(Math.random() * 1);
        // 大于等于0，小于1的double类型的随机数
        System.out.println(Math.random() * 100);
        // 求幂值
        System.out.println(Math.pow(3, 4));
        // 求较大值
        System.out.println(Math.max(10, 10.1));
        // 求较小值
        System.out.println(Math.min(1, 1.9));
        // 求绝对值
        System.out.println(Math.abs(-23));
        // 四舍五入
        System.out.println(Math.round(1.34));
    }
}
